    <div class="game-info">
        <div class="game-info__inner-wrapper">
            <div class="game-info__header">
                <h3 class="game-info__header__text">Spielziel</h3>
            </div>
            <div class="game-info__content">
                <p class="game-info__content__text">
                    Jeder Spieler erhält 14 Karten, von denen nur die letzte sichtbar ist.
                    Ziel ist es, alle Karten in diesem Stapel im Spielfeld(rechts) abzulegen.
                    Die Karten dürfen nur dann abgelegt werden, wenn bereits eine im Wert um 1 niedrigere Karte bereits im Spielfeld liegt.
                </p>
                <p class="game-info__content__text">
                    Die Ausnahmen sind Asse un Joker.
                    Asse haben den Wert 1, dürfen also sofort abgelegt werden.
                    Joker dürfen sofort in den Jokeerstapel des Spielers gelegt werden.
                </p>
            </div>
            <div class="game-info__header">
                <h3 class="game-info__header__text">Spielregeln</h3>
            </div>
            <div class="game-info__content">
                <p class="game-info__content__text">
                    Wenn der Spieler dran ist, darf er so lange Karten im Spielfeld ablegen, bis er keine passenden Karten mehr hat.
                    Dazu dürfen Karten aus der Hand, aus dem 14-Karten-Spielstapel, oder die jeweils obersten Karten aus der Ablage des Spielers verwendet werden.
                </p>
                <p class="game-info__content__text">
                    Wenn der Spieler nicht mehr im Spielfeld ablegen kann (oder will), dann muss eine Karte aus seiner Hand in die Ablage(links unten) gelegt werden.
                    Die Runde endet damit und der nächste Spieler ist dran. 
                </p>
                <p class="game-info__content__text">
                    Joker dürfen jederzeit in die Ablage gelegt werden, ohne das die Runde endet.
                </p>
            </div>
            <div class="game-info__header">
                <h3 class="game-info__header__text">Handkarten</h3>
            </div>
            <div class="game-info__content">
                <p class="game-info__content__text">
                    Zu Beginn des Spiels erhält jeder Spieler 6 Karten. Diese Karten dürfen im Spielfeld oder in der Ablage des Spielers abgelegt werden.
                </p>
                <p class="game-info__content__text">
                Sollten alle Karten innerhalb einer Runde im Spielfeld oder in der Joker-Ablage abgelegt worden sein, darf der Spieler 6 weitere Karten abheben.
                </p>
            </div>
            <div class="game-info__header">
                <h3 class="game-info__header__text">Spielfeld</h3>
            </div>
            <div class="game-info__content">
                <p class="game-info__content__text">
                    Im Spielfeld dürfen alle Karten in aufsteigender Reihenfolge abgelegt werden, beginnend mit Ass und endend mit König.
                    Die Farben der Karten haben keine Auswirkungen auf das Spiel.
                    Joker dürfen stellvertretend für alle Karten abgelegt werden, außer Ass.
                </p>
                <p class="game-info__content__text">
                Wenn ein Stapel im Spielfeld bei König angelangt, und damit voll ist, werden diese Karten automatisch wieder in den Hauptstapel eingemischt.
                </p>
            </div>
            <div class="game-info__header">
                <h3 class="game-info__header__text">Ablage</h3>
            </div>
            <div class="game-info__content">
                <p class="game-info__content__text">
                    In jeder Runde muss der Spieler eine Karte in die Ablage legen.
                    Dabei darf er einen von 4 Stapeln wählen. Damit endet eine Runde.
                </p>
                <p class="game-info__content__text">
                Die jeweils obersten Karten dürfen im Spielfeld abgelegt werden.
                </p>
                <p class="game-info__content__text">
                    Der fünfte Stapel in der Ablage ist der Jokerstapel.
                    Hier darf jederzeit aus der Hand oder aus dem 14-Karten-Spielstapel abgelegt werden, ohne das die Runde endet.
                </p>
                <p class="game-info__content__text">
                    Alle Karten in der Ablage sind immer für alle Spieler sichtbar.
                <p>
            </div>
            <div class="game-info__header">
                <h3 class="game-info__header__text">Hauptstapel</h3>
            </div>
            <div class="game-info__content">
                <p class="game-info__content__text">
                    Der Haupstapel besteht aus allen Karten, die nicht gerade im Spiel sind.
                    Die Karten im Hauptstapel sind immer verdeckt.
                </p>
                <p class="game-info__content__text">
                Vom Hauptstapel darf abgehoben werden, wenn alle Karten der Hand des Spielers abgelegt wurden, oder zu Beginn einer Runde.
                Der Spieler darf dabei nur maximal 6 Karten abheben.
                </p>
                <p class="game-info__content__text">
                    Sollten die Karten des Hauptstapels vor Spielende verbraucht sein, endet das Spiel unentschieden.
                </p>
            </div>
            <div class="game-info__header">
                <h3 class="game-info__header__text">Spielstapel</h3>
            </div>
            <div class="game-info__content">
                <p class="game-info__content__text">
                    Der Spielstapel besteht aus 14 zufälligen Karten, von denen oberste für alle sichtbar aufgedeckt wird.
                </p>
                <p class="game-info__content__text">
                    Die Karten dieses Stapels müssen im Spielfeld abgelegt werden, um zu gewinnen.
                    Die Anzahl der Karten werden neben dem Spielernamen angezeigt.
                </p>
                <p class="game-info__content__text">
                    Joker in diesem Stapel dürfen direkt in der Joker-Ablage des Spielers abgelegt werden.
                </p>
            </div>
            <div class="game-info__header">
                <h3 class="game-info__header__text">Karten</h3>
            </div>
            <div class="game-info__content">
                <p class="game-info__content__text">
                    Das Spiel benutzt zwei Sets aus 52 Standard-Pokerkarten und 4 Joker.
                    Insgesamt als 108 Karten.
                </p>
                <p class="game-info__content__text">
                    Ass hat den Wert 1 und König den Wert 13.
                    Karten dürfen im Spielfeld nur in aufsteigender Reihenfolge abgelegt werden.
                </p>
                <p class="game-info__content__text">
                    Joker dürfen anstelle von allen anderen Karten abgelegt werden, außer Ass.
                </p>
            </div>
            <div class="game-info__header">
                <h3 class="game-info__header__text">Strategie-Tipps</h3>
            </div>
            <div class="game-info__content">
                <p class="game-info__content__text">
                    Es kann sinnvoll sein, ablegbare Karten nicht sofort abzulegen, sondern in der Hand oder Ablage zu "sparen", um später mehr Karten auf einmal ablegen zu können.
                </p>
                <p class="game-info__content__text">
                Man sollte immer die sichtbaren Karten im Auge behalten, um Ablegeserien des Gegners zu verhindern.
                </p>
                <p class="game-info__content__text">
                    Karten sollten nach Möglichkeit nur in absteigender Reihenfolge in die Ablage gelegt werden, um sie später in einer aufsteigender Reihenfolge wieder abheben zu können.
                </p>
            </div>
        </div>
    </div>
</div>